Deno.test("reload: dry-run shows changes without deploying", async () => {
  const { config, cleanup } = await setupProject([
    { name: "platform", content: "v1" },
  ]);
  const runner = deploySuccessRunner();

  try {
    const results = await reloadStacks({
      config,
      runner,
      dryRun: true,
      skipGenerate: true,
    });

    assertEquals(results.length, 1);
    assertEquals(results[0].stack, "platform");
    assertEquals(results[0].action, "would-deploy");
    // In dry-run, no docker deploy should have been attempted
    assertEquals(runner.containsCommand(["docker", "stack", "deploy"]), false);
  } finally {
    await cleanup();
  }
});

