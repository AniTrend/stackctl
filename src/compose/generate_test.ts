Deno.test("generateStacks: single stack dry-run returns content", async () => {
  const tmp = await makeTempDir();
  await createFixture(tmp);

  const result = await generateStacks({ stacks: ["platform"], repoRoot: tmp, dryRun: true });

  assertEquals(result.errors, []);
  assertEquals(Object.keys(result.generated).length, 1);

  const content = result.generated["platform"];
  assertStringIncludes(content, "# Generated by stackctl generate");
  assertStringIncludes(content, "web:");
  assertStringIncludes(content, "traefik-public");

  await Deno.remove(tmp, { recursive: true });
});

