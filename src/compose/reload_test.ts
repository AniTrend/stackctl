Deno.test("reload: handles multiple stacks with mixed results", async () => {
  const { config, tmp, cleanup } = await setupProject([
    { name: "platform", content: "v1" },
    { name: "infra", content: "v1" },
  ]);
  const runner = deploySuccessRunner();

  try {
    // First run: deploy both stacks to write .rendered files
    await reloadStacks({
      config,
      runner: deploySuccessRunner(),
      skipGenerate: true,
    });

    // Modify the generated stack file for "infra" so it differs on next render
    const infraStackFile = `${tmp}/stacks/infra.yml`;
    const origContent = await Deno.readTextFile(infraStackFile);
    await Deno.writeTextFile(infraStackFile, origContent.replace('content: "v1"', 'content: "v2"'));

    // Second run: platform unchanged, infra changed
    const results = await reloadStacks({
      config,
      runner,
      skipGenerate: true,
    });

    assertEquals(results.length, 2);

    const platform = results.find((r) => r.stack === "platform")!;
    const infra = results.find((r) => r.stack === "infra")!;

    assertEquals(platform.action, "unchanged");
    assertEquals(infra.action, "deployed");
  } finally {
    await cleanup();
  }
});

