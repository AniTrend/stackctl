/**
 * Tests for the full stack generation pipeline.
 */
import { assertEquals, assertStringIncludes } from "@std/assert";
import { generateStacks } from "./generate.ts";
import type { GenerateOptions } from "./generate.ts";

async function makeTempDir(): Promise<string> {
  return await Deno.makeTempDir({ prefix: "stackctl-test-generate-" });
}

async function writeFile(dir: string, name: string, content: string) {
  await Deno.writeTextFile(dir + "/" + name, content);
}

async function createFixture(repoRoot: string) {
  const svcDir = repoRoot + "/services/web";
  await Deno.mkdir(svcDir, { recursive: true });

  await writeFile(
    svcDir,
    "docker-compose.yml",
    [
      "x-stack: platform",
      "",
      "services:",
      "  web:",
      "    image: nginx:alpine",
      "    ports:",
      '      - "8080:80"',
      "    volumes:",
      '      - "./html:/usr/share/nginx/html"',
      '      - "app-data:/var/lib/data"',
      "    deploy:",
      "      replicas: 2",
      "",
      "volumes:",
      "  app-data:",
    ].join("\n"),
  );

  await writeFile(
    svcDir,
    "swarm.fragment.yml",
    [
      "services:",
      "  web:",
      "    logging:",
      "      driver: json-file",
      "",
    ].join("\n"),
  );
}

