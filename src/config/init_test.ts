Deno.test("initConfig: force overwrites existing file", async () => {
  const tmpDir = await Deno.makeTempDir({ prefix: "stackctl-init-test-" });
  try {
    // First write an existing file
    const existingPath = join(tmpDir, ".stackctl");
    await Deno.writeTextFile(existingPath, "# existing config");

    // Without force, should error
    const resultNoForce = await initConfig({ cwd: tmpDir, force: false, dryRun: false });
    assertEquals(resultNoForce.errors.length, 1);
    assertEquals(resultNoForce.errors[0].includes("already exists"), true);

    // With force, should succeed
    const resultForce = await initConfig({ cwd: tmpDir, force: true, dryRun: false });
    assertEquals(resultForce.errors.length, 0);
    assertEquals(resultForce.written.length, 1);

    // Verify content was overwritten
    const content = await Deno.readTextFile(existingPath);
    assertStringIncludes(content, "Generated by `stackctl init`");
  } finally {
    await Deno.remove(tmpDir, { recursive: true });
  }
});

Deno.test("initConfig: detection finds docker-compose files", async () => {
  const tmpDir = await Deno.makeTempDir({ prefix: "stackctl-init-test-" });
  try {
    // Create a docker-compose.yml with services and networks
    const composeYml = join(tmpDir, "docker-compose.yml");
    await Deno.writeTextFile(
      composeYml,
      `
services:
  web:
    image: nginx
  api:
    image: my-api
networks:
  frontend:
    driver: overlay
`,
    );

    const result = await initConfig({ cwd: tmpDir, detect: true, dryRun: false });
    assertEquals(result.errors.length, 0);

    const content = await Deno.readTextFile(join(tmpDir, ".stackctl"));
    // Should have detected service names
    assertStringIncludes(content, "web");
    assertStringIncludes(content, "api");
    // Should have detected network
    assertStringIncludes(content, "frontend");
  } finally {
    await Deno.remove(tmpDir, { recursive: true });
  }
});

