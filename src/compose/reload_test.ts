Deno.test("reload: multiple deployments with only partial changes", async () => {
  const { config, tmp, cleanup } = await setupProject([
    { name: "alpha", content: "v1" },
    { name: "beta", content: "old" },
    { name: "gamma", content: "v3" },
  ]);
  const runner = deploySuccessRunner();

  try {
    // First run: deploy all three stacks so .rendered files are written
    await reloadStacks({
      config,
      runner: deploySuccessRunner(),
      skipGenerate: true,
    });

    // Modify only "beta"'s generated stack file
    const betaStackFile = `${tmp}/stacks/beta.yml`;
    const origContent = await Deno.readTextFile(betaStackFile);
    await Deno.writeTextFile(
      betaStackFile,
      origContent.replace('content: "old"', 'content: "new"'),
    );

    // Second run: alpha + gamma unchanged, beta changed
    const results = await reloadStacks({
      config,
      runner,
      skipGenerate: true,
    });

    assertEquals(results.length, 3);

    assertEquals(results.find((r) => r.stack === "alpha")!.action, "unchanged");
    assertEquals(results.find((r) => r.stack === "beta")!.action, "deployed");
    assertEquals(results.find((r) => r.stack === "gamma")!.action, "unchanged");
  } finally {
    await cleanup();
  }
});

