Deno.test("generateStacks: snapshot - generates deterministic output", async () => {
  const tmp = await makeTempDir();
  const svcDir = tmp + "/services/app";
  await Deno.mkdir(svcDir, { recursive: true });
  await writeFile(
    svcDir,
    "docker-compose.yml",
    [
      "x-stack: core",
      "",
      "services:",
      "  app:",
      "    image: nginx:alpine",
      "    ports:",
      '      - "8080:80"',
      "    deploy:",
      "      replicas: 1",
    ].join("\n"),
  );

  const outputDir = tmp + "/stacks";
  const result = await generateStacks({
    stacks: ["core"],
    repoRoot: tmp,
    outputDir,
    dryRun: false,
  });

  assertEquals(result.errors, []);
  assertEquals(result.files.length, 1);

  const generated = await Deno.readTextFile(outputDir + "/core.yml");

  const expected = [
    "# Generated by stackctl generate — do not edit manually.",
    "networks:",
    "  default:",
    "    external: true",
    "    name: traefik-public",
    "services:",
    "  app:",
    "    deploy:",
    "      replicas: 1",
    "    image: 'nginx:alpine'",
    "    logging:",
    "      driver: local",
    "      options:",
    "        max-file: 3",
    "        max-size: 10m",
    "    ports:",
    "      - '8080:80'",
  ].join("\n") + "\n";

  assertEquals(generated, expected);

  await Deno.remove(tmp, { recursive: true });
});
