import { assertEquals, assertStringIncludes } from "@std/assert";
import { parse as parseYaml } from "@std/yaml";
import { initConfig } from "./init.ts";
import { join } from "@std/path";

Deno.test("initConfig: default template contains expected sections", async () => {
  const tmpDir = await Deno.makeTempDir({ prefix: "stackctl-init-test-" });
  try {
    const result = await initConfig({ cwd: tmpDir, dryRun: false });
    assertEquals(result.errors.length, 0);
    assertEquals(result.written.length, 1);
    assertEquals(result.written[0], join(tmpDir, ".stackctl"));

    const content = await Deno.readTextFile(join(tmpDir, ".stackctl"));
    assertStringIncludes(content, "project:");
    assertStringIncludes(content, "stack:");
    assertStringIncludes(content, "directory:");
    assertStringIncludes(content, "names:");
    assertStringIncludes(content, "network:");
    assertStringIncludes(content, "render:");
    assertStringIncludes(content, "outputDirectory:");
    assertStringIncludes(content, "env:");
    assertStringIncludes(content, "activeName:");
    assertStringIncludes(content, "Generated by `stackctl init`");
  } finally {
    await Deno.remove(tmpDir, { recursive: true });
  }
});

